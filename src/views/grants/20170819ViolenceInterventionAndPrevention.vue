<h2 id="eligibility">ELIGIBILITY</h2>

<h3 id="ineligible-agencies">Ineligible agencies</h3>

<p>This solicitation is not open to Cure Violence/CeaseFire (University of Illinois at Chicago) and its sub-grantees for similar activities for the same population. </p>

<h3 id="eligible-agencies-and-gata-compliance">Eligible agencies and GATA compliance</h3>

<p>This solicitation is open to public and not-for-profit entities providing community-based violence prevention and intervention services in the State of Illinois. </p>

<h4 id="pre-registration">Pre- Registration</h4>

<p>All applicants must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov/portal">http://www.grants.illinois.gov/portal</a>. During pre-qualification, Dun and Bradstreet verifications are performed, including a check of Debarred and Suspended status and good standing with the Illinois Secretary of State.</p>

<h4 id="approved-internal-control-questionnaire">Approved Internal Control Questionnaire</h4>

<p>The pre-qualification process also includes a financial and administrative risk assessment using an Internal Controls Questionnaire (ICQ). The ICQ must be approved through the GATA portal by the date of application submission in order for an application to be reviewed.  All applications will be pre-screened for completeness and ICQ submission. Applications from agencies that have not received SFY18 ICQ approval will not be reviewed.</p>

