<h4 id="view-the-notice-of-funding-opportunity" class="text-center" style="margin-top: 15px;"><a href="/static/grants/20190111DPAservicesNOFO/DPAservicesNOFO.pdf">View the Notice of Funding Opportunity</a></h4>
<h2 id="introduction">Introduction</h2>
<p>The Illinois Criminal Justice Information Authority is requesting proposals for grants for services to families of homicide victims. Grants will be made with State Fiscal Year 2019 (SFY19) Death Penalty Abolition (DPA) funds. A total of $300,000 in funding
    is available through this solicitation for use over a period of three months. Contingent on future appropriation and satisfactory performance $1,700,000 in SFY20 DPA funds may also be available through this opportunity.</p>
<p><strong>Application deadline: 11:59 p.m., February 12, 2019.</strong></p>
<h2 id="program-requirements">Program Requirements</h2>
<p>Through this funding opportunity, ICJIA is soliciting applications proposing the provision of comprehensive services for families of victims of homicide or murder. Applicants may propose to provide a comprehensive program that provides crisis intervention,
    case management and counseling, or may propose to provide one or more components that meet the needs of the identified target population and complements existing services. Proposals that do not request funding for all three program components must
    detail how the applicant will assure that the new program will coordinate services with the existing program components.</p>
<p>Applicants proposing to provide all three program components must provide letters(s) documenting the support and cooperation of the local law enforcement, county or municipal agency which will notify the applicant of the homicide.</p>
<p>Applicants proposing to provide fewer than three program components must provide letters documenting coordination with the agencies providing the other program components.</p>
<h2 id="eligibility-information">Eligibility Information</h2>
<p>Agencies must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov">www.grants.illinois.gov</a>, to become eligible to apply for an award. Applicants must have completed
    the GATA pre-qualification process and received approval of their Internal Control Questionnaire (ICQ) by the date of application to be considered for funding. Applications from agencies that have not received ICQ approval will not be reviewed.</p>
<h2 id="eligible-applicants">Eligible Applicants</h2>
<p>Eligible applicants are limited to non-profit entities.</p>
<h2 id="deadline">Deadline</h2>
<p>Completed application materials must be emailed to <a href="mailto:CJA.DPAservicesNOFO@Illinois.gov">CJA.DPAservicesNOFO@Illinois.gov</a> by 11:59 p.m., Tuesday, February 12, 2019, to be considered for funding. Proposals will not be accepted by mail,
    fax, or in-person. Incomplete applications will not be reviewed. Late submissions will not be reviewed.</p>
<h2 id="available-funds">Available Funds</h2>
<p>Grants available through this opportunity are a portion of a SFY19 DPA funds. A maximum of $300,000 will be made available through this funding opportunity.</p>
<h3 id="period-of-performance">Period of Performance</h3>
<p>SFY19 funding is available for the period of April 1, 2019, to June 30, 2019. Based on program performance and future appropriation, ICJIA may recommend allocation of funding to support an additional 33 months of program activity. If appropriated, SFY20
    funding will be available for the period of July 1, 2019, to June 30, 2020.</p>
