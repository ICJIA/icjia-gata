<h1 class="h2 grant-main-head text-center">FFY13-16 Edward Byrne Memorial Justice  Assistance Grant Program
  Comprehensive Law  Enforcement Response to Drugs</h1>
<p>The Illinois  Criminal Justice Information
  Authority is requesting proposals for grants
  supporting a comprehensive law enforcement response to drugs.
   Grants will be  made with Federal Fiscal Year 2013-16
   (FFY13-16) Edward Byrne Memorial Justice  Assistance Grant (JAG) funds.
   A total of $4.2 million in funding in three  program categories
   is available through this solicitation for use over a period
    of 12 months. Minimum and maximum grant requests vary by the program  category.

  </p>


  <p class="text-center">
    <strong>Application  deadline: May 30, 2017</strong></p>
    <p class="text-center">
    <a href="/static/grants/JAG_Drug_NOFO/JAG_Drug_NOFO_033017.pdf"
    class="btn btn-primary btn-grant" target="_blank">
