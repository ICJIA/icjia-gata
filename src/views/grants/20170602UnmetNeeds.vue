<h1 class="spacerBottom50">{{title}}</h1>

<p>The purpose of this funding opportunity is to fund direct services for victims of crime under at least one of the three following focus areas: child abuse (minors and adults survivors of child abuse), financial crime, and impaired driving. Applicants may request a minimum of <span>$</span>75,000 to a maximum of <span>$</span>500,000 in federal funding for use over a 12-month period. The term of the grant agreement will commence upon its effective date. Based on program performance and fund availability, ICJIA may recommend allocation of funding to support an additional 24 months.</p>



<h3 id="focus-areas">FOCUS AREAS:</h3>



<h4 id="child-abuse">Child Abuse:</h4>

<p>Crimes committed by a parent, immediate family member, person responsible for the child’s welfare, individual residing in the same home as the child, or a paramour of the child’s parent in violation of Illinois criminal statutes that result in physical or emotional harm to a minor.  Covered in this funding opportunity will be minor victims and adult survivors of child abuse as defined here.</p>

<p>Child trafficking and involuntary servitude and bullying will not be addressed with this opportunity.</p>

<p>Childhood exposure to violence is not the primary intent of this opportunity, however, services for such secondary victimization are allowable if the need is identified during the provision of child abuse services.</p>



<h4 id="financial-crime">Financial Crime:</h4>

<p>Identity theft  or the financial exploitation of elders and persons with disabilities.</p>



<h4 id="impaired-driving">Impaired Driving:</h4>

<p>Impaired driving occurs when a driver operates a motor vehicle while under the influence of alcohol, drugs, or other intoxicating compounds.  This funding opportunity serves victims of motor vehicle accidents caused by impaired drivers and victims of motor vehicle accidents caused by drivers using electronic communication or video devices while driving which result in death or great bodily harm.</p>

<p>See <a href="UNMETNEEDSNOFOwattachments.pdf">Notice of Funding Opportunity</a>, for program design, staff, and training requirements.</p>



<h3 id="eligibility">ELIGIBILITY</h3>



<h4 id="ineligible-agencies">Ineligible agencies</h4>

<p>The following types of agencies are NOT eligible for this funding opportunity:</p>

<ul>
<li>Criminal justice entities, such as law enforcement, prosecution and/or court-referred services such as Court Appointed Special Advocates (CASA). Funding for criminal justice-based victim services may be offered in a future funding opportunity.</li>
<li>Under the Child Abuse Focus Area, sub-recipients of funding from Child Advocacy Centers of Illinois, the Illinois Coalition of Domestic Violence, and of the Illinois Coalition Against Sexual Assault are not eligible for funding.</li>
</ul>



<h4 id="eligible-agencies-and-gata-compliance">Eligible agencies and GATA compliance</h4>

<p>This solicitation is open to Illinois public agencies and non-profits that have completed the GATA pre-qualification process and received approval of their ICQ from a state cognizant agency by the date of application.  In addition, applicants must meet the VOCA eligibility criteria listed on pages 23-25 of the Notice of Funding Opportunity.</p>

<p>Please note applicants must Pre-register and obtain an approved Internal Control Questionnaire</p>



<h4 id="pre-registration">Pre- Registration</h4>

<p>All applicants must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov">http://www.grants.illinois.gov</a>. During pre-qualification, Dun and Bradstreet verifications are performed, including a check of Debarred and Suspended status and good standing with the Illinois Secretary of State.</p>



<h4 id="approved-internal-control-questionnaire">Approved Internal Control Questionnaire</h4>

<p>The pre-qualification process also includes a financial and administrative risk assessment using an Internal Controls Questionnaire (ICQ). The ICQ must be submitted through the GATA portal and approved by a State cognizant agency by the date of application submission in order for an application to be reviewed.  All applications will be pre-screened for completeness and ICQ approval. Applications from agencies that do not have an approved ICQ will not be reviewed.</p>



