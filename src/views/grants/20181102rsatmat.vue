              <h2 id="introduction">INTRODUCTION</h2>
<p>The Illinois Criminal Justice Information Authority (ICJIA) administers the federal Residential Substance Abuse Treatment for State Prisoners Program (RSAT) in Illinois. RSAT assists state and local governments in the development and implementation of
    substance use disorder treatment programs in state and local correctional and detention facilities. Funds also are available to create and maintain community-based aftercare services for individuals after release from incarceration. A total of approximately
    $700,000 from FFY18 funds shall be made available for state and local government agencies, $75,000 of which must be reserved for local government agencies for corrections-based or jail-based substance use disorder treatment or aftercare programs that
    meet the criteria detailed in the notice of funding opportunity. Awards range from $75,000 to $150,000.</p>
<p>Read this page and <a href="/static/grants/20181102rsatmat/RSATZipFile2018.zip"><strong>click here</strong></a> for application materials, funding eligibility, and other information.</p>
<p><strong>Application deadline: Monday, December 31, 2018, 11:59 pm.</strong></p>
<h2 id="program-requirements">PROGRAM REQUIREMENTS</h2>
<p>This grant will fund programs in the following categories which are defined below.</p>
<ol>
    <li>Corrections-based substance use disorder treatment</li>
    <li>Jail-based substance use disorder treatment</li>
    <li>Aftercare services</li>
</ol>
<p><strong>Corrections-based substance use disorder treatment</strong></p>
<p>Programs must:</p>
<ul>
    <li>Engage participants for a period between six and 12 months.</li>
    <li>Provide residential treatment facilities set apart from the general correctional population. This can be a completely separate facility or dedicated housing unit in a facility exclusively for RSAT participants.</li>
    <li>Focus on the inmate’s substance use diagnosis and addiction-related needs.</li>
    <li>Develop the inmate’s cognitive, behavioral, social, vocational, and other skills to solve the substance use and related problems.</li>
    <li>Require urinalysis and/or other proven reliable forms of drug and alcohol testing for program participants, including both periodic and random testing, and for former participants while they remain in the custody of the state or local government.</li>
    <li>Prepare participants for successful community reintegration, including post-release referral to appropriate evidence-based aftercare treatment and service providers that support the use of medication-assisted treatment.</li>
</ul>
<p>If possible, RSAT participation should be limited to inmates with six to 12 months remaining in their confinement so they can be released from prison instead of returning to the general prison population after completing the program.</p>
<p>Program design must be based on effective, scientific practices.</p>
<p><strong>Jail-based substance use disorder treatment</strong></p>
<p>Programs must:</p>
<ul>
    <li>Engage participants for at least three months.</li>
    <li>Focus on the inmate’s substance use diagnosis and addiction-related needs.</li>
    <li>Develop the inmate’s cognitive, behavioral, social, vocational, and other skills to solve the substance use and related problems with emphasis on the provision of cognitive-behavioral therapy.</li>
    <li>Require urinalysis and/or other proven reliable forms of drug and alcohol testing for program participants, including both periodic and random testing, and for former participants while they remain in the custody of the state or local government.</li>
    <li>Prepare participants for successful community reintegration, including post-release referral to appropriate evidence-based aftercare treatment and service providers that support the use of medication-assisted treatment.</li>
</ul>
<p>If possible, jail-based programs should separate the treatment population from the general correctional population and program design should be based on effective, scientific practices.</p>
<p><strong>Aftercare Services</strong></p>
<p>Substance use disorder treatment, established or implemented with assistance provided under RSAT, must provide aftercare services.</p>
<p>Programs must:</p>
<ul>
    <li>Involve coordination between the correctional treatment program, post-release supervision officer, and other social service and rehabilitation programs and services.</li>
    <li>Work in conjunction with state and local authorities and organizations involved in substance use disorder treatment to assist in the placement of program participants into community substance use disorder treatment programs or services on release.</li>
    <li>Provide reentering individuals with comprehensive care coordination prior to and upon release from the facility. This includes a comprehensive reentry plan for aftercare services, other needs, and resources. It is most successfully accomplished when
        institution staff initiates linkages with aftercare agencies and services outside of the institution, including social services and/or treatment services, parole, and case management.<sup class="footnote-ref"><a href="#fn1" id="fnref1">[1]</a></sup>        Continuity between prison and community can reduce re-offense rates.<sup class="footnote-ref"><a href="#fn2" id="fnref2">[2]</a></sup></li>
</ul>
<p>In addition:</p>
<ul>
    <li>To qualify as an aftercare program, the head of the substance use disorder treatment program must work in conjunction with state and local authorities and organizations involved in substance use disorder treatment to assist in the placement of program
        participants into community substance use disorder treatment programs or services on release.</li>
</ul>
<p>Further, programs <strong>must</strong> incorporate <strong><em>at least two forms of medication for maintenance therapy</em></strong> and <strong><em>at least one</em></strong> of the evidence-based therapies listed below. This does not include medication
    used for medical detoxification or medical withdrawal or medications only provided to pregnant women. The reason for requiring at least two forms of medication is because no single treatment is appropriate for everyone.</p>
<p><strong>Evidence-based therapies:</strong></p>
<ul>
    <li>Contingency Management (CM), including voucher- or prize-based contingency management</li>
    <li>Moral Reconation Therapy (MRT)</li>
    <li>Motivational Enhancement Therapy (MET)</li>
    <li>Motivational Interviewing (MI)</li>
    <li>Thinking for a Change (T4C)</li>
    <li>Dialectical Behavioral Therapy (DBT)</li>
    <li>Reasoning and Rehabilitation (R&amp;R)</li>
    <li>Cognitive-Behavioral Interventions—Substance Use Disorders (CBI-SA)</li>
    <li>Relapse Prevention (RP)</li>
    <li>Mind-Body Bridging Substance Abuse Program (MBBSAP)-Substance Use Disorders</li>
    <li>Family therapies, including:</li>
    <li>Functional Family Therapy(FFT)</li>
    <li>Multidimensional Family Therapy (MDFT)</li>
    <li>Multisystemic Therapy (MST)</li>
    <li>Family Behavior Therapy (FBT)</li>
    <li>Brief Strategic Family Therapy (BSFT)</li>
    <li>Therapeutic Community(TC)</li>
    <li>Matrix Model</li>
    <li>Trauma-informed substance use disorder treatment, including:</li>
    <li>Trauma-Focused Cognitive-Behavioral Therapy (TF-CBT)</li>
    <li>Seeking Safety</li>
    <li>Trauma Affect Regulation: Guide for Education and Treatment (TARGET)</li>
    <li>Trauma Recovery and Empowerment Model (TREM)</li>
</ul>
<p><strong>Aftercare only services:</strong></p>
<ul>
    <li>Assertive Community Treatment (ACT)</li>
    <li>Intensive outpatient program (IOP)</li>
    <li>Outpatient program (OP)</li>
    <li>Individual counseling</li>
    <li>Group counseling</li>
    <li>Case management</li>
    <li>Medication maintenance</li>
    <li>Vocation/Education programs and services</li>
    <li>Transitional housing or housing with supportive services</li>
    <li>Recovery support services</li>
</ul>
<p>Effective correctional and aftercare programming is most effective if they are structured, cognitive-behavioral, and skills-based. Approaches such as Alcoholics Anonymous or faith/spiritual-based services do not meet the standard for correctional or treatment
    programs.</p>
