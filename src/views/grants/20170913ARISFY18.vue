<h2>Category 1: Planning Grant</h2>
<p><em>Multi-disciplinary Planning Committee</em> Each applicant should identify a multi-disciplinary planning committee representing the criminal justice spectrum (e.g., judiciary, prosecution, defense, probation) and the community (e.g., treatment providers,
    social services, business) to guide planning. Please list titles, not proper names, of all partners.</p>
<p><em>Planning Activities</em> Ω Funds are to be used solely for the purpose of developing a local plan to implement ARI. Uses of planning grant funds may include, but are not limited to:</p>
<ul>
    <li>Employing an outside facilitator or strategic planning consultant.</li>
    <li>Conducting an outside or in-house data analysis.</li>
    <li>Providing stakeholder training.</li>
    <li>Conducting site visits to observe successful diversion programs.</li>
    <li>Community resource mapping.</li>
</ul>
<p>Funds may not be used for capital expenditures, renovations or remodeling, or any food and beverage purchases. In addition, ARI funds shall not be used to supplant existing federal, state, county, or locally funded programs.</p>
<p><em>Local Plan</em></p>
<p>At the end of the planning grant period, funded jurisdictions must complete and submit a local ARI implementation plan using the standard plan template (available at <a href="http://www.icjia.state.il.us/redeploy/standard-plan.cfm">http://www.icjia.state.il.us/redeploy/standard-plan.cfm</a>).
    Note that funded jurisdictions are under no obligation to implement the plan.</p>
<h2 id="category-2-implementation-grants">Category 2: Implementation Grants</h2>
<p>Only jurisdictions with a completed and ARIOB-approved local plan (using the standard plan template) may apply for implementation funds.</p>
<p><em>Multi-disciplinary Steering Committee</em></p>
<p>Each applicant should identify a multi-disciplinary steering committee representing the criminal justice spectrum (e.g., judiciary, prosecution, defense, probation) and the community (e.g., treatment providers, social services, business), to guide implementation.
    Please list titles, not proper names, of all partners.</p>
<p><em>Assessment of Risk, Assets and Needs</em></p>
<p>ARI sites must utilize validated assessment instruments to guide supervision and programming decisions.</p>
<p><em>Evidence-based Practices</em></p>
<p>Per the Crime Reduction Act, ARI funds must be used to invest in local programs, services and protocols that have been a promising practice by research to reduce recidivism. Examples of the evidence-based and promising practices are in Appendix B.</p>
<p><em>Performance Measurement</em></p>
<p>Every ARI site must collect and report performance measurement data quarterly as a condition of funding. The Crime Reduction Act specifically requires the following to be measured: recidivism, rate of revocations, employment rates, education achievement,
    successful completion of substance abuse treatment programs, and payment of victim restitution. ARI and ICJIA have defined additional performance measures to track these and other indicators of impact. The list of mandatory data elements to be collected
    are included in Appendix C.</p>
<p><em>Reduction Goal &amp; Reimbursement Provision</em></p>
<p>In exchange for the funds, jurisdictions must agree to reduce by 25 percent the number of offenders committed to IDOC from a defined target population or else face a penalty. The reduction goal is based on the jurisdictions’ average number of commitments
    over the past three years according to IDOC data. Once granted funds, sites are required to collect data and regularly report on progress. Jurisdictions having difficulty meeting their reduction goals are provided technical assistance. They can propose
    a corrective action plan to meet an adjusted target or, as a penalty, prepare to reimburse a portion of the grant at the discretion of the ARIOB Reimbursement is capped at $2,500 for each person short of the goal.</p>
<p><em>Problem-Solving Court Certification (if applicable)</em></p>
<p>If the proposed program model for ARI implementation is a problem-solving court (PSC), the jurisdiction must either be certified or in the process of being certified through the Administrative Office of the Illinois Courts. ARI funds can be used to support
    initial staffing, training and administrative support throughout the certification process; however, no funds can be used to provide services until such time as the PSC is certified and able to enroll participants.</p>
<h2 id="eligibility">Eligibility</h2>
<p>Only county units of government are eligible to apply for funds under this notice. A county may submit on behalf of a group of counties or a judicial circuit. Private agencies and not-for-profit organizations are not eligible to apply.</p>
<p>Agencies must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov">www.grants.illinois.gov</a>, to become eligible to apply for an award. Applicants must have completed
    the GATA pre-qualification process and received approval of their Internal Control Questionnaire (ICQ) by the date of application to be considered for funding. Applications from agencies that have not received ICQ approval will not be reviewed.</p>
<h2 id="deadline">Deadline:</h2>
<p>Completed application materials must be emailed to <a href="mailto:CJA.ARI.SFY18PIGrant@Illinois.gov">CJA.ARI.SFY18PIGrant@Illinois.gov</a> by 11:59 p.m., October 30, 2017, to be considered for funding. Proposals will not be accepted by mail, fax, or
    in-person. Incomplete applications will not be reviewed. Late submissions will not be reviewed.</p>
<h2 id="available-funds">Available Funds:</h2>
<p>$500,000 will be made available through this funding opportunity for up to 10 planning and/or implementation grants. Grants will be made with SFY18 ARI funds.</p>
<h2 id="period-of-performance">Period of Performance:</h2>
<p>Funding is available for the period of January 1, 2018 to June 30, 2018.</p>
