<h1 id="notice-of-funding-opportunity-federal-fiscal-year-victim-of-crime-act-2015-lead-entity-services-for-victims-of-sexual-assault-program" class="spacerBottom50">FEDERAL FISCAL YEAR VICTIM OF CRIME ACT 2015 LEAD ENTITY SERVICES FOR VICTIMS OF SEXUAL ASSAULT PROGRAM</h1>

<p>ICJIA is the state agency charged with the administration of the Illinois’ Victims of Crime Act Victim Assistance Formula Grant Program. This program is supported by fines and penalties levied against criminals convicted of federal crimes and allocated to states by formula by the Office for Victims of Crime of the U.S. Department of Justice. In federal fiscal year 2015, Illinois received a VOCA award of $77.5 million. </p>

<p>VOCA grants must support the provision of direct services to victims of violent crime. States are required to allocate a minimum of 10 percent of funds received for services to each of the following: victims of sexual assault, domestic violence, child abuse, and underserved victims of violent crime. VOCA funds may not be used to supplant or replace state and local funds that would otherwise be available for crime victim services and must be used to develop new projects or expand existing projects.</p>

<p>For more information on the VOCA Program please visit: <a href="http://ojp.gov/ovc/about/victimsfund.html">http://ojp.gov/ovc/about/victimsfund.html</a>.   </p>

<p>ICJIA recently completed a statewide assessment of victim services in Illinois. The report was presented to the Ad Hoc Victim Services Committee for consultation and coordination with other state agencies and victim stakeholder groups. The final report’s recommendations were approved by ICJIA Board in January 2017. These recommendations outline a comprehensive plan to address victims’ needs in Illinois and are reflected in this funding opportunity where appropriate. </p>

<p>Please see the following link to access the report and recommendations: <a href="http://www.icjia.state.il.us/articles/ad-hoc-victim-services-committee-research-report">http://www.icjia.state.il.us/articles/ad-hoc-victim-services-committee-research-report</a>.</p>

<p>As part of the recommendations established by the Victim Services Ad Hoc Committee, the strengthening and expansion of core services for victims of crime was a priority.  To fulfill this priority, ICJIA has made available $16.3 million in VOCA grant funding for core services and related support costs for victims of sexual assault.  These funds will be made available to the Illinois Coalition Against Sexual Assault. </p>

