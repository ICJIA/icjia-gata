      <br><br>.</p>

<h2 class="h3 grant-section-head first" id="program-requirements">Program Requirements</h2>
<div class="grant-section-context">
<p>Applicants  may request funding under one or more categories of funding. </p>
<p><strong>Category 1: Law Enforcement Deflection and  Diversion Programs </strong></p>
<p>To be  eligible for funding, diversion/deflection programs must:</p>
<ul>
  <li>Be  run by a local law enforcement agency, county sheriff’s department, or a  multi-jurisdictional cooperative law enforcement unit for which an Implementing  Agency has been identified. </li>
  <ul>
    <li>An  Implementing Agency in this context is a government agency which will, on  behalf of the multi-jurisdictional unit, apply for the grant, enter into the  inter-governmental grant agreement with the Authority, accept and account for  grant funds, and meet all Authority reporting and compliance requirements.</li>
  </ul>
  <li>Have  developed a plan to make potential clients and referral sources aware of the  services available.</li>
  <li>Have  a plan to coordinate program activities with community agencies in the service  area, including substance abuse treatment, medical care and supportive service  providers as well as relevant government agencies.</li>
</ul>
<p><strong>Category 2: Multi-jurisdictional drug  prosecution units </strong></p>
<p>To be  eligible for funding, drug prosecution programs must:</p>
<ul>
  <li>Be  run by a county state’s attorney’s office or a county office of the state’s  attorney’s appellate prosecutor.</li>
  <li>Have  a written statement demonstrating collaborative involvement with one or more  drug trafficking enforcement unit, as defined in Category 3.</li>
</ul>
<p><strong>Category 3: Multi-Jurisdictional Large-Scale Drug  Trafficking Enforcement </strong></p>
<p>To be eligible for  funding, drug trafficking enforcement programs must: <strong> </strong></p>
<ul>
  <li>Be  run by a cooperative unit comprised of three or more law enforcement agencies  for which an Implementing Agency has been identified.</li>
  <ul>
    <li>An  Implementing Agency in this context is a government agency which will, on  behalf of the multi-jurisdictional unit, apply for the grant; enter into the  inter-governmental grant agreement with the Authority; accept and account for  grant funds; and meet all Authority reporting and compliance requirements.</li>
  </ul>
  <li>Have  a written interagency agreement between all participating agencies defining  activities and responsibilities.</li>
  <li>Have  a prosecutor statement demonstrating collaborative involvement with the member  agencies toward meeting the unit’s goals and objectives.</li>
  <li>Have  a plan of intelligence sharing via computerized networking.</li>
  <li>Have  a plan for forfeiture sharing, including how funds will be used. </li>
  <li>Have  a commitment for staffing by all participating agencies.</li>
</ul>
</div>
<h2 class="h3 grant-section-head" id="additional-requirements">Additional  Requirements: Law Enforcement Task Forces </h2>
<div class="grant-section-context">
<p>All members  of a law enforcement task force supported with these funds who is a task force  commander, agency executive, task force officer, or other task force member of  equivalent rank, will complete required online internet-based task force  training once during the life of the grant award. The training is provided free  of charge online through the U.S. Bureau of Justice Assistance Center for Task Force  Integrity and Leadership (<a href="http://www.ctfli.org"><strong>www.ctfli.org</strong></a>). </p>
<p>This  training addresses task force effectiveness as well as other key issues  including privacy and civil liberties/rights, task force performance  measurement, personnel selection, and task force oversight and accountability. </p>
<p>In addition,  the Authority will require the submission of task force personnel rosters that  include training course completion certificates. Go to <a href="http://www.ctfli.org"><strong>www.ctfli.org</strong></a> for more information. </p>
</div>
