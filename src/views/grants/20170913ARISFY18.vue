<h2 id="adult-redeploy-illinois-sfy18-planning-and-implementation-grants">Adult Redeploy Illinois – SFY18 Planning and Implementation Grants</h2>
<p><a href="/static/grants/20170913ARISFY18/ARIsfy18plNOFO.pdf">Notice of Funding Opportunity</a></p>
<p>The Illinois Criminal Justice Information Authority is requesting proposals for Adult Redeploy Illinois – State Fiscal Year 2018 (SFY18) Planning and Implementation Grants. Grants will be made with SFY18 ARI funds. A total of $500,000 in funding for Planning
    and Implementation Grants is available through this solicitation for use over a period of 6 months.</p>
<h3 id="application-deadline-october-30-2017">Application deadline: October 30, 2017</h3>
<h2 id="program-requirements">Program requirements:</h2>
<p>Two categories of funding are available through this solicitation, for either the development or implementation of an ARI local plan. Awards resulting from this opportunity will have a projected period of performance of January 1, 2018, to June 30, 2018.</p>
