<p>The Illinois Criminal Justice Information Authority is requesting proposals for SFY19 Adult Redeploy Illinois planning grants. A total of $200,000 in SFY19 Adult Redeploy Illinois funding is available through this solicitation for use over a six-month
    period.</p>
<p>County applicants may request a planning grant to convene stakeholders, analyze data, and conduct other activities in the design of a local ARI program to divert probation-eligible offenders from prison to community-based supervision and services. As
    part of the planning grant process, applicants will be expected to gather and analyze relevant criminal justice system data, including, but not limited to, current services offered, services needed, strategies for service delivery, potential eligible
    populations, local governance issues, data collection and analysis capabilities, and estimated costs of alternatives to incarceration. The process culminates in the development and submission of a local ARI implementation plan.</p>
<p><strong>Application deadline: October 26, 2018</strong></p>
<h2 id="program-requirements">Program Requirements</h2>
<p><em>Planning Multi-Disciplinary Team</em></p>
<p>Each applicant should identify a multi-disciplinary team (MDT) to oversee and execute the planning process, representing the criminal justice spectrum (e.g., judiciary, prosecution, defense, probation) and the community (e.g., treatment providers, social
    services, business) to guide planning. Please list titles, not proper names, of all partners.</p>
<p><em>Evidence-based Practices</em></p>
<p>ARI funds must be used to invest in local programs, services, and protocols that have been demonstrated by research to reduce recidivism.</p>
<p><em>Planning Activities</em></p>
<p>Funds are to be used solely for developing a local plan to implement ARI to expand alternatives to incarceration. Uses of planning grant funds may include, but are not limited to:</p>
<p>• Employing an outside facilitator or strategic planning consultant.</p>
<p>• Conducting an outside or in-house data analysis.</p>
<p>• Providing stakeholder training.</p>
<p>• Conducting site visits to observe successful diversion programs.</p>
<p>• Community resource mapping.</p>
<p>Funds may not be used for capital expenditures, renovations or remodeling, or any food and beverage purchases. In addition, ARI funds shall not be used to supplant existing federal, state, county, or locally funded programs.</p>
<p><em>Local Plan</em></p>
<p>At the end of the planning period of performance, funded jurisdictions must complete and submit a local ARI implementation plan using the standard plan template (available at <a href="http://www.icjia.state.il.us/redeploy/standard-plan.cfm">http://www.icjia.state.il.us/redeploy/standard-plan.cfm</a>).
    Jurisdictions are under no obligation to implement the plan; however, a letter stating why the plan will not be implemented, or a detailed explanation with supporting evidence as to why a plan was not developed, must be submitted by the end of the
    performance period in conjunction with periodic progress reports, on or before July 15, 2019.</p>
<h2 id="eligibility-information">Eligibility Information</h2>
<p>Agencies must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov">www.grants.illinois.gov</a> , to become eligible to apply for an award. Applicants must have completed
    the GATA pre-qualification process and received approval of their Internal Control Questionnaire (ICQ) by the date of application to be considered for funding. Applications from agencies that have not received ICQ approval will not be reviewed.</p>
<h2 id="eligible-applicants">Eligible Applicants</h2>
<p>Only county units of government are eligible to apply for funds under this notice. A county may submit on behalf of a group of counties or a judicial circuit. Private agencies or not-for-profit organizations are not eligible to apply. Applicants for funding
    must be a county unit of government in good standing with the State of Illinois.</p>
<h2 id="deadline">Deadline</h2>
<p>Completed application materials must be emailed to <a href="mailto:CJA.ARISFY19NOFO2@illinois.gov">CJA.ARISFY19NOFO2@illinois.gov</a> by <strong>11:59 p.m., October 26, 2018,</strong> for funding consideration. Proposals will not be accepted by mail,
    fax, or in-person. Incomplete applications will not be reviewed. Late submissions will not be reviewed.</p>
<h2 id="available-funds">Available Funds</h2>
<p>A total of $200,000 dollars will be made available through this funding opportunity.</p>
<h2 id="period-of-performance">Period of Performance</h2>
<p>Funding is available for the period of January 1, 2019, to June 30, 2019. Based on program performance and fund availability, the ARI Oversight Board may recommend allocation of funding to support an additional 24 months.</p>
