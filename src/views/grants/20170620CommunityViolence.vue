<p>The purpose of this grant opportunity is to fund direct services for victims of community violence. Applicants may request a minimum of <span>$</span>75,000 to a maximum of $1,000,000 in federal funding for use over a 12-month period. The term of the grant agreement will commence upon its effective date. Based on program performance and fund availability, ICJIA may recommend allocation of funding to support an additional 24 months.</p>



<h3 id="community-violence-defined">COMMUNITY VIOLENCE DEFINED:</h3>

<p>For purposes of this solicitation, Community Violence includes adult victims of violent crime  and minor victims who directly experience or witness violent crime in the home, school, work or neighborhood.  </p>

<p>In addition, domestic and sexual violence are included under this focus area with the following parameters: <br>
<ul>
  <li>Domestic and sexual violence should not be the primary focus of victimization under this funding. Agencies who wish to primarily serve victims of these crimes, should apply for funding through the Illinois Coalition Against Domestic Violence and/or the Illinois Coalition Against Sexual Assault.  </li>
<li>Domestic and sexual violence victimization may emerge when serving community violence victims. Therefore applicants must develop a written agreement and working relationship with experienced domestic and sexual violence agencies so that clients have easy access to staff appropriately trained in domestic and sexual violence victim services. Applications must include a signed letter of agreement in which both parties agree to provide domestic and sexual violence consultation and/or training when needed, and referral process where appropriate. </li>
<li>The applicant agency will refer clients to a specialized victim service agency in cases where it is appropriate. For example, referral to a specialized victim service agency must take place if child sexual abuse emerges in the course of service delivery and agency staff do not have the clinical skills to address it. </li>
</ul>

<p>See the <a href="/static/grants/2017CommunityViolence/VOCAComunityViolenceFinalNOFO062017.pdf">Notice of Funding Opportunity</a>, for program design, staff, and training requirements.</p>



<h3 id="eligibility">ELIGIBILITY</h3>



<h4 id="ineligible-agencies">Ineligible agencies</h4>

<p>The following types of agencies are NOT eligible for this funding opportunity:</p>

<ul>
<li>Criminal justice entities, such as law enforcement, prosecution and/or court-referred services such as Court Appointed Special Advocates (CASA). Funding for criminal justice-based victim services may be offered in a future funding opportunity.</li>
</ul>



<h4 id="eligible-agencies-and-gata-compliance">Eligible agencies and GATA compliance</h4>

<p>This solicitation is open to Illinois public agencies and non-profits that have completed the GATA pre-qualification process and received approval of their ICQ from a state cognizant agency by the date of application.  In addition, applicants must meet the VOCA eligibility criteria listed on pages 23-25 of the Notice of Funding Opportunity.</p>

<p>Please note applicants must Pre-register and obtain an approved Internal Control Questionnaire</p>



<h5 id="pre-registration">Pre- Registration</h5>

<p>All applicants must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov">http://www.grants.illinois.gov</a>. During pre-qualification, Dun and Bradstreet verifications are performed, including a check of Debarred and Suspended status and good standing with the Illinois Secretary of State.</p>



<h5 id="approved-internal-control-questionnaire">Approved Internal Control Questionnaire</h5>

<p>The pre-qualification process also includes a financial and administrative risk assessment using an Internal Controls Questionnaire (ICQ). The ICQ must be submitted through the GATA portal and approved by a State cognizant agency by the date of application submission in order for an application to be reviewed.  All applications will be pre-screened for completeness and ICQ approval. Applications from agencies that have not received approval of their SFY18 ICQ will not be reviewed.</p>



