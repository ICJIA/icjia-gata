<h2 id="introduction">Introduction</h2>
<p>The Illinois Criminal Justice Information Authority is requesting proposals for grants funding training of law enforcement personnel. Grants will be made with State Fiscal Year 2019 Death Penalty Abolition (DPA) funds. A total of $4.5 million in funding is available through this solicitation for use over a period of two months. Contingent on future appropriation and satisfactory performance, State Fiscal Year 2020 DPA funds also may be made available through this opportunity.</p>
<p><strong>Application deadline: 11:59 p.m., March 18, 2019.</strong></p>
<h2 id="program-requirements">Program Requirements</h2>
<p>Through this funding opportunity, ICJIA is soliciting applications proposing to provide training to law enforcement personnel. Proposals must be received from the agencies that will be delivering all three of the following components of the training program:</p>
<ul>
    <li>Planning and provision of training.</li>
    <li>Training supervision and oversight.</li>
    <li>Assessment of training effectiveness.</li>
</ul>
<h2 id="eligibility-information">Eligibility Information</h2>
<p>Agencies must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov">www.grants.illinois.gov</a>, to become eligible to apply for an award. Applicants must have completed the GATA pre-qualification process and received approval of their State Fiscal Year 2019 Internal Control Questionnaire (ICQ) by the date of period of performance start date, May 1, 2019, to be considered for funding.</p>
<h2 id="eligible-applicants">Eligible Applicants</h2>
<p>Eligible applicants must meet the following requirements:</p>
<ul>
    <li>Applicants must have experience providing training to law enforcement personnel.</li>
    <li>Applicants must be one of these types of agencies and institutions:<br>
        o Governmental Agencies, including state and local law enforcement agencies, and the Illinois Law Enforcement Training and Standards Board.<br>
        o Educational Institutions.<br>
        o Non-profit Agencies, including the Mobile Training Units.</li>
</ul>
<h2 id="deadline">Deadline</h2>
<p>Completed application materials must be emailed to <a href="mailto:CJA.DPAtrainingNOFO@Illinois.gov">CJA.DPAtrainingNOFO@Illinois.gov</a> by 11:59 p.m., Monday, March 18, 2019, to be considered for funding. Proposals will not be accepted by mail, fax, or in-person. Incomplete applications will not be reviewed. Late submissions will not be reviewed.</p>
<h2 id="available-funds">Available Funds</h2>
<p>A total of $4.5 million in DPA funds are available through this NOFO. Awards totaling a minimum of $10,000 and maximum of $1 million will be made.</p>
<h3 id="period-of-performance">Period of Performance</h3>
<p>Funding is available for the period of May 1, 2019, to June 30, 2019. Based on program performance and future appropriation, ICJIA may recommend allocation of funding to support an additional 34 months of program activity. If appropriated, SFY20 funding will be available for the period of July 1, 2019, to June 30, 2020.</p>
