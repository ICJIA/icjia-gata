            <h2 id="introduction">Introduction</h2>
            <p>The Illinois Criminal Justice Information Authority is requesting proposals for continuing support for nine current
              prosecution, public defense and court programs currently supported with ICJIA-administered JAG funds. Grants
              will be made with Federal Fiscal Year 2014-16 (FFY14-16) Edward Byrne Memorial Justice Assistance Grant (JAG)
              funds. A total of $600,000 in funding is available through this solicitation for use over a period of 12 months.
              Awards resulting from this NOFO will range from a minimum of $40,000 to a maximum of $225,000.</p>
            <p>
              <strong>Application deadline: 11:59 p.m., April 16, 2018.</strong>
            </p>
            <h2 id="program-requirements">Program Requirements</h2>
            <p>Applicants may request funding for up to 12-months of program activity. Applications must:</p>
            <ul>
              <li>Demonstrate the effectiveness of the current program; and</li>
              <li>Include the agency’s active participation in a data-driven planning process that objectively assesses jurisdictional
                or agency needs.</li>
            </ul>
            <p>Support for program beyond this bridge funding is contingent on:</p>
            <ul>
              <li>Completion of a planning component.</li>
              <li>Demonstrated ongoing need for the program.</li>
              <li>Satisfactory performance.</li>
              <li>Availability of JAG funds.</li>
            </ul>
            <p>Applicants are required to set reasonable, quantifiable benchmarks for program performance by completing the
              Goals, Objectives and Performance Metrics table for the requested project. As this opportunity may continue
              support for a range of program types, the objectives and performance measures must be crafted to address the
              unique activities of each project. Applicants should be cognizant of supplanting restrictions detailed in the
              NOFO. However, all projects funded must address the common goal defined in the NOFO.</p>
            <p>Funded programs will be required to submit quarterly progress reports that will at a minimum include these objectives.
              Applicants may list additional support service objectives for the program.</p>
            <h2 id="eligibility">Eligibility Information</h2>
            <p>Agencies must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal,
              <a href="http://www.grants.illinois.gov">www.grants.illinois.gov</a>, to become eligible to apply for an award. Applicants must have completed the GATA
              pre-qualification process and received approval of their Internal Control Questionnaire (ICQ) by the date of
              application to be considered for funding. Applications from agencies that have not received ICQ approval will
              not be reviewed.</p>
            <h2 id="eligible-applicants">Eligible Applicants</h2>
            <p>Eligible applicants are limited to counties and State agencies requesting continuing funding for prosecution,
              public defense, and courts programs currently supported by ICJIA-administered JAG funds. The eligible programs
              are:
            </p>
            <ul>
