            <h2 id="program-requirements">PROGRAM REQUIREMENTS</h2>
            <p>Programs must provide a meaningful response to the needs of sexual assault or domestic violence victim populations
              through coordinated responses from community law enforcement agencies, prosecutors, courts/probation, victim
              services agencies, and other state agencies and departments. The applicant must demonstrate that it has the
              expertise in providing relevant and accessible community-based outreach and intervention services or has the
              capacity to link to existing services in the community tailored to the needs of the population. Applicants
              must also have implemented or plan to work toward fully implementing best practices.</p>
            <p>A maximum of $650,000 from Federal Fiscal Years 2017, 2016, 2015 and 2014 funds shall be made available to
              local agencies for multi-disciplinary programs that meet the following criteria:</p>
            <ul>
              <li>One Stop Services – Co-location of victim service advocates and law enforcement, either at a central location
                such as a family justice center or at the county justice center. If co-location does not currently exist,
                applicant must demonstrate how they are working toward implementation.</li>
              <li>Specialized Training – The Applicant must develop and execute a plan for the cross-training of law enforcement,
                first responders, and community-based advocacy, which includes the identification of each discipline’s roles
                and procedures related to domestic violence and/or sexual assault.</li>
              <li>Program Assessment – All VAWA funded programs must demonstrate progress toward meeting project goals and
                objectives annually, and describe and justify any planned program modifications to continuously improve</li>
              <li>Implementation Plan - Provide a step by step plan of how the program or project will be implemented. Include
                the personnel title of the person, who will be responsible for the associated work for the program or project,
                resources to be used and anticipated results.
              </li>
