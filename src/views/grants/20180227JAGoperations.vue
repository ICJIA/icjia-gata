              <li>Office of the State Appellate Prosecutor Systemic Sentencing Appeals Program</li>
              <li>Office of the Cook County State’s Attorney Human Trafficking Program</li>
              <li>Office of the Cook County State’s Attorney Community Justice Center Program</li>
              <li>Office of the Cook County Public Defender DNA Program</li>
              <li>Office of the Cook County Public Defender Mitigation Program</li>
              <li>Winnebago County Youth Recovery Court</li>
              <li>Center of Excellence</li>
              <li>Coles/Cumberland Sex Offender Supervision program</li>
            </ul>
