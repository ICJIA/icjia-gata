            <h2 id="program-requirements">PROGRAM REQUIREMENTS</h2>
            <p>Applicants may request funding under one or more categories.</p>
            <p><em><strong>Model 1</strong></em></p>
            <p><strong>Post-Overdose Response.</strong> This model features a response initiated by a law enforcement
              officer after
              emergency administration of medication to reverse overdose. The post-overdose response may be
              multi-disciplinary and
              may include the distribution
              of naloxone, in-person contact, relationship building, and access to treatment. Example: Quick Response
              Team.</p>
            <p><em><strong>Model 2</strong></em></p>
            <p><strong>Self-Referral Response.</strong> This response is initiated by an individual by going to a
              police station or
              otherwise contacting a police officer or law enforcement agency and acknowledging their own substance
              misuse and need
              for treatment. The
              response offers individuals assistance in obtaining substance use disorder treatment. Examples: Safe
              Passage, A Way
              Out.</p>
            <p><em><strong>Model 3</strong></em></p>
            <p><strong>Active Outreach Response.</strong> This response is initiated by a police officer through
              proactive
              identification of persons likely to misuse substances or have a substance use disorder. Law enforcement
              intentionally
              seeks out and identifies
              individuals who may need help, and a warm handoff is made to a service provider who engages them in
              treatment.</p>
            <p><em><strong>Model 4</strong></em></p>
            <p><strong>Community Engagement Response.</strong> This response is initiated by a police officer or law
              enforcement
              agency in response to a notification from a community member when no criminal charges are present.</p>
            <p><em><strong>Model 5</strong></em></p>
            <p><strong>Officer Intervention Response.</strong> These are commonly referred to as pre-arrest diversion
              programs. The
              response occurs when criminal charges are present but held in abeyance or citations issued with
              requirement of
              treatment.</p>
