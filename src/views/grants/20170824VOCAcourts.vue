<h2 id="law-enforcement-prosecution-and-county-casa-victim-assistance-program">Law Enforcement, Prosecution, and County-CASA Victim Assistance Program</h2>

<p>The Illinois Criminal Justice Information Authority is requesting proposals to fund direct services for victims of crime through municipal police departments, county sheriff departments, county state’s attorney’s offices, and county CASA programs.  Grants will be made with Federal Fiscal Year 2016 (FFY16) Victims of Crime Act (VOCA) funds. A total of $6.0 million in funding is available through this solicitation for use over a period of 12 months.</p>



<h2 id="available-funds">Available Funds:</h2>

<p>Applicants may request a minimum of $40,000 and a maximum of \$1,500,000 to be spent over a 12-month period. The terms of the grant agreement will commence upon its effective date. Depending on program performance and fund availability, ICJIA may approve funding to support an additional 24 months. <a href="/static/grants/20170824VOCAcourts/VOCAcourtsNOFO.pdf">Notice of Funding Opportunity</a>, for program design, staff, and training requirements.</p>



<h2 id="matching-funds">Matching funds:</h2>

<p>A 20 percent cash or in-kind match requirement will be imposed on grant funds under this program. Match funds must be spent in support of the program’s goals and objectives. </p>



<h2 id="eligibility">ELIGIBILITY</h2>



<h3 id="eligible-agencies">Eligible agencies</h3>

<p>Applicants must apply for funding to support either a Law Enforcement/Prosecution-Based Victim Assistance Program or a Court Appointed Special Advocacy (CASA) Program. Programs within eligible agencies will be allowed to apply for funding for staffing, staff training, supervision of direct services, and office equipment to create or expand existing victim service programs. </p>

<p>All activities supported with this award must fall outside of the normal scope of law enforcement and prosecution activities. Grant funds may not be used to facilitate participation of witness-victims in criminal justice proceedings. Victim eligibility for services cannot be contingent upon participation in the criminal justice process. Services also must be made available after a victim’s involvement with the criminal justice system has ended through either the applicant agency or referral to a victim service provider.</p>

<p>Read the complete <a href="/static/grants/20170824VOCAcourts/VOCAcourtsNOFO.pdf">Notice of Funding Opportunity</a>  <br>
 for all eligibility requirements. </p>



<h3 id="gata-compliance">GATA compliance</h3>

<p>All applicants must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov/portal">http://www.grants.illinois.gov/portal</a>. During pre-qualification, Dun and Bradstreet verifications are performed, including a check of Debarred and Suspended status and good standing with the Illinois Secretary of State.</p>



<h4 id="approved-internal-control-questionnaire">Approved Internal Control Questionnaire</h4>

<p>The pre-qualification process also includes a financial and administrative risk assessment using an Internal Controls Questionnaire (ICQ). The ICQ must be approved through the GATA portal by the date of application submission in order for an application to be reviewed.  All applications will be pre-screened for completeness and ICQ approval. Applications from agencies that have not received SFY18 ICQ approval will not be reviewed.</p>



<h2 id="notice-of-funding-opportunity">NOTICE OF FUNDING OPPORTUNITY</h2>

<p>Read the <a href="/static/grants/20170824VOCAcourts/VOCAcourtsNOFO.pdf">Notice of Funding Opportunity</a>. </p>



<h3 id="deadline">Deadline</h3>

<p>Applications are due at 11:59 p.m., October 15, 2017. Completed application materials must be emailed to <a href="mailto: cja.vocagrantsunit@illinois.gov">cja.vocagrantsunit@illinois.gov</a>.</p>



<h3 id="notice-of-intent">NOTICE OF INTENT</h3>

<p>Agencies interested in submitting an application MUST complete an online Notice of Intent form by 11:59 p.m., October 2, 2017. </p>

<p><a href="https://www.surveygizmo.com/s3/3787821/VOCA-LAW-ENFORCEMENT-PROSCETUTION-AND-COUNTY-CASA-VICTIM-ASSISTANCE-PROGRAM-NOFO-Notice-of-Intentnt">NOTICE OF INTENT </a></p>



<h4 id="period-of-performance">Period of Performance</h4>

<p>The projected period of performance for awards resulting from this opportunity is January 1, 2018, through December 31, 2018. Contingent on satisfactory performance and available funds, ICJIA staff may recommend to its Board a maximum of two 12-month grants for each project.  </p>



<h4 id="contact-information">Contact Information</h4>

<p>Questions may be submitted via email at <a href="mailto: cja.vocagrantsunit@illinois.gov">cja.vocagrantsunit@illinois.gov</a>.</p>

<p>The deadline for submitting questions is 11:59 p.m., October 12, 2017. All substantive questions and responses will be posted on the Authority website at <a href="https://grants.icjia.cloud/">https://grants.icjia.cloud/</a>. Due to the competitive nature of this solicitation, applicants may not discuss this opportunity directly or indirectly with any Authority employee other than the respondent of this email address. Only written answers to questions shall be binding on the state. </p>



