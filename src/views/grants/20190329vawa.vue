<h3 class="text-center" style="margin-bottom: 30px;"><a href="/static/grants/20190329vawa/VAWANOFO.pdf">Link to NOFO</a></h3>
<p>The Illinois Criminal Justice Information Authority is requesting applications for grants for the Services for Underserved Areas and Victim Groups Program. Grants will be made with FFY18 Violence Against Women Act funds. A total of $1.4 million is available through this solicitation for use over a period of 12 months.</p>
<p><strong>Application deadline:</strong> Application materials must be received by <strong>11:59 p.m. on Tuesday, April 30, 2019</strong>.</p>
<h2 id="program-requirements">Program Requirements</h2>
<p>As part of the recommendations established by the Victim Services Ad Hoc Committee, enhancing victim safety and providing access to services for underserved communities is a priority. To fulfill this priority, ICJIA has made available VAWA grant funding for victims of domestic violence and sexual assault in underserved communities and victim groups.</p>
<h2 id="eligibility-information">Eligibility Information</h2>
<p>Agencies must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="https://grants.illinois.gov/portal/">https://grants.illinois.gov/portal/</a>, to become eligible to apply for an award. Applicants must have completed the GATA pre-qualification process and received approval of their Internal Control Questionnaire (ICQ) by the date of period of performance start date, July 1, 2019 to be considered for funding.</p>
<h2 id="eligible-applicants">Eligible Applicants</h2>
<p>This solicitation is open not for profit corporations that administer state-wide community-based domestic violence programs, which provide advocacy, technical assistance, training, public information about domestic violence issues, in-person counseling, information and referral, community education, professional training, institutional advocacy and provide services to victims of domestic violence in underserved communities.</p>
<p>It is also open for not for profit corporations that administer state-wide sexual assault crisis centers, which provide comprehensive sexual assault services: 24-hour hotline, 24-hour criminal justice and medical advocacy services, in-person counseling, information and referral, community education, professional training, institutional advocacy, and provide services to victims in underserved communities or victim groups. In addition, applicants must administer state and federal funds to its members, set standards for services and monitor each center to ensure program and fiscal accountability.</p>
<h2 id="deadline">Deadline</h2>
<p>Completed application materials must be emailed to <a href="mailto:2019VAWANOFO@Illinois.gov">2019VAWANOFO@Illinois.gov</a> by <strong>11:59 p.m., Tuesday, April 30, 2019,</strong> to be considered for funding. Proposals will not be accepted by mail, fax, or in-person. Incomplete applications will not be reviewed. Late submissions will not be reviewed.</p>
<h2 id="available-funds">Available Funds</h2>
<p>Grants available through this funding opportunity are a portion of FFY18 Violence Against Women Act funds. A minimum of $14.1 million will be made available through this funding opportunity.</p>
<h2 id="period-of-performance">Period of Performance</h2>
<p>Funding is available for the period of July 1, 2019, through June 30, 2020.</p>
