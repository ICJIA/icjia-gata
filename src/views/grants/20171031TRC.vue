<h2 id="introduction">Introduction</h2>
<p>The purpose of this funding opportunity is to serve hard-to-reach victims of crime through Illinois Level 1 Adult or Pediatric Trauma Centers. Applicants may request a maximum of $1.1 million in federal funding for use over a 15-month period. The term
    of the grant agreement will commence upon its effective date. Based on program performance and fund availability, ICJIA may recommend allocation of funding to support an additional 24 months.</p>
<p>For the purposes of this funding opportunity, the following definitions will apply:</p>
<p><strong>Trauma Recovery Center (TRC) Model</strong>: A comprehensive service delivery model providing coordinated clinical and case management services for adult and/or minors victims of violent crime and their families who typically do not access services
    due to individual and cultural barriers to help seeking.</p>
<p><strong>Historically underserved or marginalized victims</strong>: Historically underserved or marginalized victims may not seek services for a variety of reasons, including stigma, stereotypes, and social norms that deter help seeking; a lack of knowledge
    on service availability, methods of access, and eligibility; concerns or past experiences with provider insensitivity to disabilities or cultural beliefs; and a lack of positive experience with the criminal justice system, including law enforcement,
    which is a frequent access point to victim services. Historically underserved or marginalized victims who may benefit from the low-barrier services that the TRC aims to provide may include individual who are homeless, individuals who are chronically
    mentally ill, immigrants and refugees, individuals who are disabled, people with severe trauma-related symptoms, and individuals of diverse ethnicities/origins.</p>
<p><strong>Direct services</strong>: Efforts that respond to the emotional, psychological and physical needs of crime victims; assist victims to stabilize their lives after victimization; assist victims to understand and participate in the criminal justice
    system; or provide victims of crime with a measure of safety.</p>
<p><strong>See Notice of Funding Opportunity for program design, staff, and training requirements.</strong></p>
<h2 id="eligibility">Eligibility</h2>
<p>Eligible applicants include <strong>public or non-profit Illinois hospitals with a Level 1 Adult or Pediatric Trauma Center designation by the Illinois Department of Public Health</strong>. In addition, applicants must meet the VOCA eligibility criteria
    listed on pages 20-21 of the Notice of Funding Opportunity.</p>
<p>This solicitation is open to Illinois public agencies and non-profits that have completed the GATA pre-qualification process and received approval of their ICQ from a state cognizant agency by the date of application.</p>
<p><strong>Please note: applicants must pre-register and obtain an approved Internal Control Questionnaire.</strong></p>
<h3 id="pre-registration">Pre-Registration</h3>
<p>All applicants must be pre-qualified through the Grant Accountability and Transparency Act (GATA) Grantee Portal, <a href="http://www.grants.illinois.gov">www.grants.illinois.gov</a>. During pre-qualification, Dun and Bradstreet verifications are performed,
    including a check of Debarred and Suspended status and good standing with the Illinois Secretary of State.</p>
<h3 id="approved-internal-control-questionnaire">Approved Internal Control Questionnaire</h3>
<p>The pre-qualification process also includes a financial and administrative risk assessment using an Internal Controls Questionnaire (ICQ). <strong>The ICQ must be submitted through the GATA portal and approved by a State cognizant agency by the date of application submission in order for an application to be reviewed.</strong>    All applications will be pre-screened for completeness and ICQ approval. Applications from agencies that do not have an approved ICQ will not be reviewed.</p>
